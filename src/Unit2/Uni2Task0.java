package Unit2;

public class Uni2Task0 {
    public static void main(String[] args) {
        int a = 0;
        while (a < 11) {
            System.out.print(a + " ");
            a++;
        }
        System.out.println();
        a = 0;
        do {
            System.out.print(a + " ");
            a++;
        } while (a < 11);
        System.out.println();
        for (a = 0; a < 11; a++) {
            System.out.print(a + " ");
        }
    }
}
